public class Adder {
    private int a;
    private int b;

    public int add(int a,int b){
        return a + b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public int getA() {
        return a;
    }
}
